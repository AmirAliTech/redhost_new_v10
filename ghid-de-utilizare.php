                    <h4>Creare sau modificare conturi de e-mail</h4>
                    <p class="text-wrap w-100 ">Utilizati Mail -&gt; Manage/Add/Remove Accounts -&gt; Add Account. Introduceti numele de cont (spre exemplu 'office' daca doriti sa creati contul de e-mail office@domeniulDvs.tld), indicati daca doriti ca respectivul cont sa nu depaseasca o anumita dimensiune in MB si parola. Apasati 'Add account'. Dupa aceasta operatiune va puteti verifica contul nou creat prin WebMail (vedeti mai jos) sau puteti sa configurati un client de mail local (spre exemplu Outlook Express).</p>

                    <h4>Verificarea adreselor de e-mail prin WebMail</h4>
                    <p class="text-wrap w-100 ">Utilizati Webmail, alegeti unul dintre programele disponibile (SquirrelMail sau Horde) si continuati dupa cum vi se indica. O cale alternativa de verificare a Webmail-ului (important in cazul in care aveti mai multi utilizatori de email si nu doriti sa le oferiti acces in cPanel la toti) este sa folositi www.domeniu.tld/webmail in loc de www.domeniu.tld/cpanel (de exemplu www.redhost.ro/webmail). Ca si login introduceti adresa de email completa (de exemplu office@domeniu.tld) iar ca parola, parola pe care ati stabilit-o la crearea contului respectiv de email. De asemenea trebuie ales softul de Webmail si urmati pasii solicitati.</p>

                    <h4>Modificare parola cPanel</h4>
