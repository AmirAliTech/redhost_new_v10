                    <p class="text-wrap w-100 ">In cazul in care aveti si alte domenii decat cel principal si doriti sa redirectati vizitatorii acestora catre site-ul principal, folositi in acest scop sectiunea Parked Domains. In cazul in care doriti ca domeniile respective sa corespunda unor site-uri proprii (si ati ales un pachet de gazduire care sa includa numarul necesare de Addon Domains), folositi sectiunea Addon Domains. In ambele cazuri, domeniile respective trebuie sa fie deja inregistrate, nameserverele acestora sa indice serverele noastre (sectiunea II. de mai sus) si propagarea sa se fi realizat.</p>


