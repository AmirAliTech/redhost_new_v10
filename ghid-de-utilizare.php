                    <h4>Realizare backupuri
                    </h4>
                    <p class="text-wrap w-100 ">Desi firma noastra isi mentine propriile backupuri acestea sunt strict destinate uzului intern (doar pentru cazul in care se defecteaza un HDD sau se corupe un sistem de fisiere). In concluzie este foarte indicat ca la momente cheie (de exemplu la incarcarea unei noi versiuni a site-ului sau in cazul altor modificari) sa realizati si dvs copii de siguranta proprii. Puteti accesa cele 4 tipuri de backupuri posibile dupa ce v-ati autentificat in cPanel, in sectiunea Backups. Puteti realiza backupuri pentru directorul dumneavoastra de utilizator (care cuprinde fisierele web - din public_html, conturile de mail cu tot cu setarile aferente, statistici), pentru bazele de date (atentie: acest backup nu include si userii MySQL si parolele acestora) si pentru filtrele si alias-urile definite de catre utilizatori. In acelasi timp mai puteti crea si descarca un download complet al contului insa spre deosebire de celelalte tipuri de backup-uri, trebuie sa luati legatura cu noi pentru restaurare (numai administratorul poate restaura un backup complet).
